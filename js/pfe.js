export class pfe{

    static calcular(eg, _pfe){

        /* 10 90 */
        let a = [97, 121, 150, 185, 227, 275, 331, 398, 471, 556, 652, 758, 876, 1004, 1145, 1294, 1453, 1621, 1794, 1973, 2154, 2335, 2513, 2686, 2851, 2985]
        let b = [137, 171, 212, 261, 319, 387, 467, 559, 665, 784, 918, 1068, 1234, 1416, 1613, 1824, 2049, 2285, 2530, 2781, 3036, 3291, 3543, 3786, 4019, 4234]

        if (eg < 15 || eg > 40) {  
            return 0
        } else {
            eg = parseInt(eg);
            eg = eg - 15;

            let uno = b[eg] - a[eg];
            let dos = _pfe - a[eg];
            return Math.trunc(80 / (uno) * (dos)) + 10
        }
    }
}